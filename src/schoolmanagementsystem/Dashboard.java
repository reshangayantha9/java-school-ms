    void sCount(){
        int c=0;
        try {
            String sql = "select*from student";
            ResultSet rs = MyConn.search(sql);
            
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("s_id"));
                c +=1;
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        jLabel4.setText("'"+c+"'");
    }
