    void notice(){
        try {
            String sql = "select*from notice ORDER BY n_id DESC LIMIT 10";
            ResultSet rs = MyConn.search(sql);
             DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            int a = df.getRowCount();
            for (int i = 0; i < a; i++) {
                df.removeRow(0);
            }
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("n_name"));
                v.add(rs.getString("n_desc"));
                df.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
