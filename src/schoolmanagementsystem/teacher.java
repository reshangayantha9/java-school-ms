    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String g = "";
        if (jRadioButton1.isSelected()) {
            g = "male";
        } else if (jRadioButton2.isSelected()) {
            g = "female";
        }else{
            JOptionPane.showMessageDialog(null,"Please Select your Gender");
        }
        try {
            String sql = "update teacher set t_name='" + jTextField2.getText() + "',t_age='" + jTextField3.getText() + "',t_gender='" + g + "',t_address='" + jTextField4.getText() + "',t_tp='" + jTextField5.getText() + "' where t_id='" + jTextField1.getText() + "'";
            MyConn.save(sql);
            progress();
            JOptionPane.showMessageDialog(null, "'" + jTextField2.getText().toString() + "' Teacher Updated");
            jProgressBar1.setValue(0);
            data();
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

