        void data() {
        try {
            String sql = "select*from teacher";
            ResultSet rs = MyConn.search(sql);
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            int a = df.getRowCount();
            for (int i = 0; i < a; i++) {
                df.removeRow(0);
            }
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("t_id"));
                v.add(rs.getString("t_name"));
                v.add(rs.getString("t_age"));
                v.add(rs.getString("t_gender"));
                v.add(rs.getString("t_address"));
                v.add(rs.getString("t_tp"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

